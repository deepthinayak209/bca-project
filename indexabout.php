                <p>Sovereign Tile Works, a tile factory in Mangalore was started as a proprietary concern by <b>Late Sri. Sujir Keshav Nayak</b> in the year 1929. Sri. Nayak was working as a partner along with his cousin C.V. Nayak in Mangalore before the establishment in 1929. He rendered his service as a partner for 10 years.</p>
                
              </div>
                    </div>
